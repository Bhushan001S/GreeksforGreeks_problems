class Solution {
  public:
    pair<long long, long long> getMinMax(vector<long long int> arr) {
        long long int mini=INT_MAX;
        long long int maxi=INT_MIN;
        
        for(long long int i:arr){
            if(mini>i) mini=i;
            if(maxi<i) maxi=i;
        }
        return {mini,maxi};
    }
};

